/**
 * 
 * Program to print below pyramid structure
 *      (4 space)_ _ _ _ 1          
        (3 space)_ _ _  1 2        
        (2 space)_ _   1 2 3       
        (1 space)_    1 2 3 4      
        (0 space)    1 2 3 4 5     
*/


function printPyramid(num){

    for(let i=1 ; i<=num;i++){

        let res='';

        for(let j=1; j<=num ;j++){ 

          if(j <= num - i){          
            res+=' ';
          }else{
            res+= (j - (num-i)) +' ';  
          }
        }

        console.log(res);
    }
}

printPyramid(5)

// Outer loop → i = 1 to 5 (rows)
// Inner loop → j = 1 to 5 (columns)


